def metrics_lineal(df, xcol, ycol, nombre):
    # Filtrar válidos
    d = df[[xcol, ycol]].dropna()
    x = d[xcol].values
    y = d[ycol].values

    # Ajuste lineal y = m*x + b
    m, b = np.polyfit(x, y, 1)
    y_hat = m * x + b

    # Métricas
    r2 = r2_score(y, y_hat)
    rmse = np.sqrt(mean_squared_error(y, y_hat))

    print(f"[{nombre}] y = {m:.6f} x + {b:.6f} | R² = {r2:.5f} | RMSE = {rmse:.5f}")

