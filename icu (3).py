# @title
import numpy as np
import matplotlib.pyplot as plt
from sklearn.metrics import r2_score, mean_squared_error

# --- Datos (usa tu DF 'Datos') ---
d1 = Datos[['X1','Y1']].dropna(); x1, y1 = d1['X1'].values, d1['Y1'].values
d2 = Datos[['X2','Y2']].dropna(); x2, y2 = d2['X2'].values, d2['Y2'].values

# --- Ajustes originales ---
m1, b1 = np.polyfit(x1, y1, 1); y1_hat = m1*x1 + b1
r2_1 = r2_score(y1, y1_hat); rmse_1 = np.sqrt(mean_squared_error(y1, y1_hat))

m2, b2 = np.polyfit(x2, y2, 1); y2_hat = m2*x2 + b2
r2_2 = r2_score(y2, y2_hat); rmse_2 = np.sqrt(mean_squared_error(y2, y2_hat))

# --- Elegir mejor y peor ---
if r2_1 >= r2_2:
    # Mejor: X1–Y1 ; Peor: X2–Y2
    m_best, b_best = m1, b1
    xw, yw = x2, y2
    mw_orig, bw_orig = m2, b2
    r2_w_orig, rmse_w_orig = r2_2, rmse_2
    lab_mejor, lab_peor = "X1–Y1", "X2–Y2"
else:
    # Mejor: X2–Y2 ; Peor: X1–Y1
    m_best, b_best = m2, b2
    xw, yw = x1, y1
    mw_orig, bw_orig = m1, b1
    r2_w_orig, rmse_w_orig = r2_1, rmse_1
    lab_mejor, lab_peor = "X2–Y2", "X1–Y1"

# --- Reajuste paralelo sobre la 'peor' (pendiente fija = m_best) ---
b_par = yw.mean() - m_best * xw.mean()
y_par = m_best * xw + b_par
r2_par = r2_score(yw, y_par)
rmse_par = np.sqrt(mean_squared_error(yw, y_par))

# --- Preparar líneas para graficar ---
xline_best = np.linspace(
    (x1.min() if lab_mejor == "X1–Y1" else x2.min()),
    (x1.max() if lab_mejor == "X1–Y1" else x2.max()),
    100
)
yline_best = m_best * xline_best + b_best

xline_peor = np.linspace(xw.min(), xw.max(), 100)
yline_peor_orig = mw_orig * xline_peor + bw_orig
yline_peor_par  = m_best   * xline_peor + b_par

# --- UNA SOLA FIGURA ---
plt.figure(figsize=(10,6))

# Puntos
plt.scatter(x1, y1, color='#1f77b4', alpha=0.85, edgecolor='k', label='Datos X1–Y1')
