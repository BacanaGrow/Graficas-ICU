# Línea de la mejor (negra)
plt.plot(xline_best, yline_best, color='black', linewidth=2,
         label=f"Mejor {lab_mejor}: y={m_best:.4f}x+{b_best:.4f}")

# Línea original de la 'peor' (gris punteado)
plt.plot(xline_peor, yline_peor_orig, '--', color='gray',
         label=f"Original {lab_peor}: y={mw_orig:.4f}x+{bw_orig:.4f}\nR²={r2_w_orig:.4f} | RMSE={rmse_w_orig:.4f}")

# Línea paralela reajustada de la 'peor' (verde)
plt.plot(xline_peor, yline_peor_par, color='green', linewidth=2,
         label=f"Paralela {lab_peor}: y={m_best:.4f}x+{b_par:.4f}\nR²={r2_par:.4f} | RMSE={rmse_par:.4f}")

